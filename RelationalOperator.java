class RelationalOperator
{
    public static void main(String A[])
    {
        boolean result;
        result = (10 == 10);
        System.out.println("The result is : "+result);

        result = (5 < 5);
        System.out.println("The result is : "+result);

        result = (5 <= 5);
        System.out.println("The result is : "+result);

        result = (10 > 5);
        System.out.println("The result is : "+result);

        result = (10 >= 5);
        System.out.println("The result is : "+result);

        result = (2 != 2);
        System.out.println("The result is : "+result);

    }
}